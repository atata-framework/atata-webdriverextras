namespace Atata
{
    /// <summary>
    /// Specifies the visibility of the element.
    /// </summary>
    public enum Visibility
    {
        /// <summary>
        /// Finds the elements with any visibility.
        /// </summary>
        Any,

        /// <summary>
        /// Finds only the visible elements.
        /// </summary>
        Visible,

        /// <summary>
        /// Finds only the hidden elements.
        /// </summary>
        Hidden
    }
}
